-module(txt_to_morse).
-export([morse_convert/0,mOrse_convert/1]).

morse_convert() ->
   #{"a" => ".- ","b" =>"-... ","c" =>"-.-. ","d" => "-.. ","e" => ". ","f" => "..-. ","g" => "--. ","h" =>".... ","i" =>".. ","j" =>".--- ","k" =>"-.- ","l" =>".-.. ","m" =>"-- ","n" =>"-. ","o" => "--- ","p" =>".--. ","q" =>"--.- ","r" =>".-. ","s" =>"... ","t" =>"- ","u" =>"..- ","v" =>"...- ","w" =>".-- ","x" =>"-..- ","y" =>"-.--","z" =>"--.. ","1" => ".----","2" => "..---","3" => "...--","4" => "....-","5" => ".....","6" => "-....","7" => "--...","8" => "---..","9" => "----.","0" => "-----","\." => ".-.-.-" ,"\," => "--..--" ,"\;" =>"---..." ,"\?" =>"..--..","\'" => ".----.","\-" => "-....-","\/" =>"-..-.","\(" =>"-.--.-","\)" =>"-.--.-","\"" =>".-..-."}.
 %C = [[C] || C <- Str],
mOrse_convert([]) -> io:fwrite("~n");
mOrse_convert([First | Last]) ->
   O = [maps:get([First], morse_convert())],
   io:fwrite("~s~n",[O]),
  mOrse_convert(Last).
